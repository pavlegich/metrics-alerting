func main() {
	var checks []*analysis.Analyzer

	checks = append(checks,
		// анализаторы из golang.org/x/tools/go/analysis/passes
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildssa.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		ctrlflow.Analyzer,
		deepequalerrors.Analyzer,
		errorsas.Analyzer,
		fieldalignment.Analyzer, // сортировка полей в структурах
		findcall.Analyzer,
		framepointer.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		pkgfact.Analyzer,
		printf.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer, // ложные shadow срабатывания с err
		shift.Analyzer,
		sigchanyzer.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		tests.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
		// собственный анализатор
		linter.ExitChecker,
		// публичные анализаторы
		bodyclose.Analyzer,
		errwrap.Analyzer,
	)

	// Добавление анализаторов staticheck
	analyzers := map[string]bool{
		"S1020":  true,
		"ST1003": true,
		"QF1003": true,
	}
	for _, v := range staticcheck.Analyzers {
		if strings.Contains(v.Analyzer.Name, "SA") {
			checks = append(checks, v.Analyzer)
		}
	}
	for _, v := range simple.Analyzers {
		if analyzers[v.Analyzer.Name] {
			checks = append(checks, v.Analyzer)
			break
		}
	}
	for _, v := range stylecheck.Analyzers {
		if analyzers[v.Analyzer.Name] {
			checks = append(checks, v.Analyzer)
			break
		}
	}
	for _, v := range quickfix.Analyzers {
		if analyzers[v.Analyzer.Name] {
			checks = append(checks, v.Analyzer)
			break
		}
	}

	multichecker.Main(
		checks...,
	)
}
